package com.todaysoft.ghealth.mybatis.model;

public class GradeInterval
{
    private int grade;
    
    private Double min;
    
    private Double max;
    
    public int getGrade()
    {
        return grade;
    }
    
    public void setGrade(int grade)
    {
        this.grade = grade;
    }
    
    public Double getMin()
    {
        return min;
    }
    
    public void setMin(Double min)
    {
        this.min = min;
    }
    
    public Double getMax()
    {
        return max;
    }
    
    public void setMax(Double max)
    {
        this.max = max;
    }
}
