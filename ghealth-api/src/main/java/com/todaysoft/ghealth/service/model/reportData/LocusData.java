package com.todaysoft.ghealth.service.model.reportData;

public class LocusData
{
    private String geneName;
    
    private String snps;
    
    private String geneResult;
    
    private String geneRisk;
    
    public String getGeneName()
    {
        return geneName;
    }
    
    public void setGeneName(String geneName)
    {
        this.geneName = geneName;
    }
    
    public String getSnps()
    {
        return snps;
    }
    
    public void setSnps(String snps)
    {
        this.snps = snps;
    }
    
    public String getGeneResult()
    {
        return geneResult;
    }
    
    public void setGeneResult(String geneResult)
    {
        this.geneResult = geneResult;
    }
    
    public String getGeneRisk()
    {
        return geneRisk;
    }
    
    public void setGeneRisk(String geneRisk)
    {
        this.geneRisk = geneRisk;
    }
}
